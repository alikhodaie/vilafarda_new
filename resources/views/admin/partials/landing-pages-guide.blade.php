        راهنمای مدیر — صفحات لندینگ سئو
    </h6>

    <p class="small mb-2"><strong>روش پیشنهادی (فیلتر از جستجو):</strong></p>
    <ol class="mb-3 small ps-3">
        <li class="mb-1">در سایت بروید به <strong>لیست اقامتگاه‌ها</strong> (<code>/homes</code>).</li>
        <li class="mb-1">فیلترها را بزنید: شهر، استخر، امکانات دقیق (مثل استخر سرپوشیده)، نوع ویلا و …</li>
        <li class="mb-1">دکمه جستجو را بزنید تا لیست درست شود.</li>
        <li class="mb-1"><strong>آدرس نوار مرورگر</strong> را کپی کنید (کل URL).</li>
        <li class="mb-1">اینجا در فیلد <strong>«لینک فیلتر جستجو»</strong> بچسبانید و فرم را ذخیره کنید.</li>
        <li class="mb-1">عنوان H1 و اسلاگ را بنویسید؛ مثال: «ویلاهای استخردار تهران» → <code>ویلاهای-استخردار-تهران</code></li>
    </ol>

    <p class="small mb-2"><strong>نمونه لینک:</strong></p>
    <p class="small mb-3" dir="ltr"><code>/homes?province=8&amp;city=301&amp;features[]=pool</code></p>

    <ul class="mb-2 small ps-3">
        <li class="mb-1"><strong>اسلاگ:</strong> آدرس نهایی لندینگ → <code>/l/اسلاگ</code> (جدا از لینک فیلتر).</li>
        <li class="mb-1"><strong>امکانات دقیق:</strong> اگر در جستجو امکان خاصی (مثل استخر سرپوشیده) با تیک جدا انتخاب شده، در URL معمولاً <code>options[]=شناسه</code> می‌آید — همان را کپی کنید.</li>
        <li class="mb-1"><strong>فقط شهر انتخاب‌شده:</strong> برای لندینگ شهر روشن بماند تا فقط همان شهر نمایش داده شود.</li>
        <li class="mb-1"><strong>تاریخ رزرو</strong> در URL ذخیره نمی‌شود (برای سئو ثابت نمی‌ماند).</li>
        <li class="mb-1">اگر لینک فیلتر خالی باشد، از فیلدهای استان/شهر/نوع پایین فرم استفاده می‌شود.</li>
        <li class="mb-0"><strong>فعال:</strong> بعد از تست لیست، تیک فعال را بزنید.</li>
    </ul>
