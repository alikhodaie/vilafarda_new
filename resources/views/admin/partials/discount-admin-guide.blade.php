        <li class="mb-1"><strong>کد تخفیف:</strong> یکتا و لاتین/عدد باشد (مثلاً <code>NAAB1404</code>). مهمان همین کد را در مرحله پرداخت وارد می‌کند.</li>
        <li class="mb-1"><strong>نوع:</strong> «درصد» حداکثر ۹۹٪؛ «مبلغ» به تومان از کل مبلغ رزرو کم می‌شود (اگر مبلغ تخفیف از قیمت بیشتر باشد، سقف اعمال می‌شود).</li>
        <li class="mb-1"><strong>ثبت‌نامی جدید:</strong> کد فقط برای کاربرانی که <em>بعد از ساخت این تخفیف</em> در سایت ثبت‌نام کنند فعال می‌شود.</li>
        <li class="mb-1"><strong>کاربران (موجود):</strong> می‌توانید همه، میزبان‌ها، کسانی که سفارش داشتند/نداشتند، یا لیست دستی را انتخاب کنید. برای «سفارش داشتند» بازهٔ تاریخ ورود/رزرو را مشخص کنید.</li>
        <li class="mb-1"><strong>تاریخ انقضا:</strong> بعد از این روز کد قابل استفاده نیست (پایان همان روز).</li>
        <li class="mb-1"><strong>پیامک:</strong> اختیاری؛ در صورت انتخاب «الگو» یا «کاستوم»، فقط به کاربرانی که در لیست تخفیف قرار گرفتند ارسال می‌شود (برای ثبت‌نامی جدید، پیامک هنگام ساخت ارسال نمی‌شود).</li>
        <li class="mb-0"><strong>مصرف:</strong> هر کاربر معمولاً یک‌بار می‌تواند کد را در پرداخت استفاده کند. از لیست تخفیف‌ها می‌توانید مصرف دستی را ثبت کنید.</li>
